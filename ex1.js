
 // Initialize two variables with numeric values

 let number1 = 10; // First number
 let number2 = 20; // Second number

 // Function to add two numbers
 function addNumbers(num1, num2) {
     return num1 + num2;
 }

 // Calculate the sum of the two numbers
 let sum = addNumbers(number1, number2);

 // Write the output on the web page
document.write("<h2>Addition Example</h2>");
document.write("<p>First number: " + num1 + "</p>");
document.write("<p>Second number: " + num2 + "</p>");
document.write("<p>Sum: " + sum + "</p>");
